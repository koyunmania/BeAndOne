package com.beone.webapp.model;

public class BeOneCalendarSubCategoryTranslation {
	private int translationId;
	private String calendarSubCategoryTrans;
	private String descriptionTrans;
	private int subcategoryId;
	private int languageId;
	
	public int getTranslationId() {
		return translationId;
	}
	public void setTranslationId(int translationId) {
		this.translationId = translationId;
	}
	public String getCalendarSubCategoryTrans() {
		return calendarSubCategoryTrans;
	}
	public void setCalendarSubCategoryTrans(String calendarSubCategoryTrans) {
		this.calendarSubCategoryTrans = calendarSubCategoryTrans;
	}
	public String getDescriptionTrans() {
		return descriptionTrans;
	}
	public void setDescriptionTrans(String descriptionTrans) {
		this.descriptionTrans = descriptionTrans;
	}
	public int getSubcategoryId() {
		return subcategoryId;
	}
	public void setSubcategoryId(int subcategoryId) {
		this.subcategoryId = subcategoryId;
	}
	public int getLanguageId() {
		return languageId;
	}
	public void setLanguageId(int languageId) {
		this.languageId = languageId;
	}
}
